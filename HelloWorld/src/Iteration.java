
public class Iteration {

	public static void main(String[] args) {

		int i = 0;
		int m = 100;
		for (i = 0; i <= 10; i++) {
			System.out.print(i + ", ");
		}

		System.out.println();

		for (m = 100; m < 1000; m++) {
			System.out.print(m + ", ");

		}

		System.out.println();

		System.out.println();

		System.out.println();
	}

}
